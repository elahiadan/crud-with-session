     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Product $product)
    {
        $i = 1;
        $pro = $request->input('name');
        foreach ($pro as $item) {
            $data = Product::where('id', $i)->first();
            $data->row1 = $item[0];
            $data->row2 = $item[1];
            $data->row3 = $item[2];
            $data->save();
            $i++;
        }
        return redirect('product');
    }

